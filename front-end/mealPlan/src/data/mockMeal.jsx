export const fetchMealDetail = async (id) => {
  // Simulate API delay
  await new Promise((resolve) => setTimeout(resolve, 500))

  const meal = mockMeals.find((meal) => meal.id === Number.parseInt(id))

  if (!meal) {
    throw new Error("Meal not found")
  }

  return meal
}

export const fetchRelatedMeals = async (currentMealId, limit = 4) => {
  // Simulate API delay
  await new Promise((resolve) => setTimeout(resolve, 300))

  // Filter out current meal and get random related meals
  const availableMeals = mockMeals.filter((meal) => meal.id !== Number.parseInt(currentMealId))

  // Shuffle array and take first 'limit' items
  const shuffled = availableMeals.sort(() => 0.5 - Math.random())
  const relatedMeals = shuffled.slice(0, limit)

  return relatedMeals
}
